package Operators;

public class Variable_Values {

	public static void main(String[] args) {
		
		
		int k=90;
		
		int l=20;
		
		k=k-l;//  70
		k=30;// 30
		k=l;// 20
		
		System.out.println(l);//  
		
		System.out.println(k);// 30  1
		
		k=k+l+k;// 20+20+20=60
		
		k=k+l;//  60+20
		
		System.out.println(k);//  80    
		
	}

}
